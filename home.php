<h2 align="center"><b>  Knowledge Attitude Practise (KAP) tentang IKN dan Pembangunan Berkelanjutan </b> </h2>
</br></br></br>
<h3 align="justify">

1. Tujuan Pembangunan Berkelanjutan ke-4: Pendidikan Berkualitas. Dalam kategori ini, IKN Nusantara dapat berperan dalam mendukung akses dan kualitas pendidikan melalui institusi pendidikan dan program pelatihan yang ditawarkan di kompleks ini. IKN Nusantara memberikan kesempatan bagi siswa, mahasiswa, dan masyarakat umum untuk memperoleh pendidikan dan keterampilan yang relevan dengan kebutuhan industri.
</br></br>
2. Tujuan Pembangunan Berkelanjutan ke-7: Energi Terbarukan dan Terjangkau. IKN Nusantara dapat berkontribusi pada tujuan ini dengan mendorong penggunaan energi terbarukan di kompleks industri. Dalam operasionalnya, kompleks ini dapat mengoptimalkan penggunaan energi terbarukan seperti tenaga surya atau tenaga angin, serta menerapkan praktik efisiensi energi yang berkelanjutan. Dengan demikian, IKN Nusantara berperan dalam mengurangi ketergantungan pada sumber energi fosil dan berkontribusi pada mitigasi perubahan iklim.
</br></br>
3. Tujuan Pembangunan Berkelanjutan ke-8: Pertumbuhan Ekonomi yang Berkelanjutan dan Pekerjaan Layak. IKN Nusantara memberikan dampak ekonomi positif dengan menciptakan lapangan kerja baru melalui industri yang beroperasi di kompleks ini. Hal ini berkontribusi pada pertumbuhan ekonomi yang berkelanjutan dan penciptaan pekerjaan yang layak bagi masyarakat.
</br></br>
4. Tujuan Pembangunan Berkelanjutan ke-9: Industri, Inovasi, dan Infrastruktur. IKN Nusantara berkontribusi pada tujuan ini dengan menjadi pusat pengembangan industri yang mendorong inovasi, kolaborasi antara industri dan institusi pendidikan, serta menyediakan infrastruktur yang mendukung pertumbuhan sektor industri di Indonesia.
</br></br>
5. Tujuan Pembangunan Berkelanjutan ke-11: Kota dan Permukiman yang Inklusif, Aman, Tahan Bencana, dan Berkelanjutan. IKN Nusantara dapat berkontribusi pada tujuan ini dengan mengembangkan kompleks industri yang inklusif dan berkelanjutan, menyediakan infrastruktur yang aman, serta memperhatikan aspek keberlanjutan dalam perencanaan dan pengelolaan kompleks.
</br></br>
6. Tujuan Pembangunan Berkelanjutan ke-12: Konsumsi dan Produksi yang Bertanggung Jawab. IKN Nusantara dapat memainkan peran penting dalam mendorong praktik konsumsi dan produksi yang bertanggung jawab di sektor industri. Hal ini dapat dicapai dengan menerapkan prinsip-prinsip ekonomi sirkular, mengurangi limbah, dan meningkatkan efisiensi dalam rantai pasok dan proses produksi. Selain itu, IKN Nusantara juga dapat mendorong adopsi praktik berkelanjutan seperti penggunaan bahan baku yang ramah lingkungan dan pengelolaan limbah yang baik.
</br></br>
7. Tujuan Pembangunan Berkelanjutan ke-17: Kemitraan untuk Mencapai Tujuan. IKN Nusantara merupakan hasil kerja sama antara pemerintah Indonesia dan Korea Selatan, yang menunjukkan pentingnya kemitraan dan kerja sama internasional dalam mencapai tujuan pembangunan berkelanjutan. Melalui kolaborasi antara berbagai pemangku kepentingan, termasuk pemerintah, perusahaan, institusi pendidikan, dan masyarakat, IKN Nusantara dapat menciptakan lingkungan yang mendukung pertumbuhan ekonomi berkelanjutan dan implementasi SDGs secara holistik.
</h3>