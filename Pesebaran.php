<h3 align="justify"> Wilayah Penting <b>IKN Nusantara</b></h3>

<br/>
<h4 align="justify"> IKN direncanakan akan dibagi menjadi 3 Zonasi yaitu: <br/><br/>

1. Kawasan Inti Pusat Pemerintahan dengan luas sekitar 5.600 Ha <br/>
2. Kawasan Ibu Kota Negara dengan luas sekitar 56.000 Ha (termasuk kawasan inti Pusat Pemerintahan) <br/>
3. Kawasan Perluasan Ibu Kota Negara dengan luas sekitar 256.142,74 Ha (Bappenas, 2020) <br/>
4. Kawasan Ibu Kota Negara sendiri dibagian atas 6 Bagian Wilayah Perencanaan (BWP) yaitu: <br/>

<br/><br/>
BWP I (Pusat) Pusat Pemerintahan <br/>
BWP II (Barat) Pusat Ekonomi <br/>
BWP III (Selatan) Pusat Pemerintahan <br/>
BWP IV (Tenggara) Pusat Hiburan <br/>
BWP V (Timur) Pusat Pendidikan <br/>
BWP VI (Utara) Pusat Inovasi & Riset <br/><br/>

Menurut Menteri Pekerjaan Umum dan Perumahan Rakyat (PUPR) Basuki Hadimuljono memastikan pembangunan Ibu Kota Negara (IKN) Nusantara akan dimulai pada pertengahan tahun 2022. Semoga dengan pembangunan IKN ini bisa berdampak pada pemerataan pembangunan dan kesejahteraan di Indonesia.
</h4>

